class MaxHeap {
    constructor() {
        this.data = [null];
    }

    insert(x) {
        this.data.push(x);
        this.swim();
    }

    swim() {
        let idx = this.data.length - 1;
        while (true) {
            let pid = idx >> 1;
            if (idx > 1 && this.data[pid] < this.data[idx]) {
                this.swap(pid, idx);
                idx = pid;
            } else {
                break;
            }
        }
    }

    remove(x) {
        if (this.data.length === 1) return;
        const ans = this.data[1];
        this.swap(1, this.data.length - 1);
        this.data.length -= 1;
        this.sink();
        return ans;
    }

    sink() {
        let idx = 1;
        while (2 * idx < this.data.length) {
            let leftIdx = 2 * idx,
                rightIdx = leftIdx + 1;
            let maxIdx = idx;
            if (this.data[maxIdx] < this.data[leftIdx]) maxIdx = leftIdx;
            if (
                rightIdx < this.data.length &&
                this.data[maxIdx] < this.data[rightIdx]
            )
                maxIdx = rightIdx;
            if (maxIdx === idx) break;
            this.swap(maxIdx, idx);
            idx = maxIdx;
        }
    }

    swap(a, b) {
        let tmp = this.data[a];
        this.data[a] = this.data[b];
        this.data[b] = tmp;
    }

    size() {
        return this.data.length - 1;
    }
}
